package cts.basics;

import java.util.Scanner;

public class Fibonacci {

	public static void main(String[] args) {
		
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter the number");
		
		int a=sc.nextInt();
		int t=0,r=1;	
		for(int i=1;i<=a;i++)
		{
			System.out.print(t+" ");
			int sum=t+r;
			t=r;
			r=sum;
		}

	
	}

	

}
