package cts.basics;

public class SwapTwo {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		System.out.println("before swapping ");
		System.out.println(a);
		System.out.println(b);
		
		int temp;
		temp=a;
		a=b;
		b=temp;
		System.out.println("after swapping ");
		System.out.println(a);
		System.out.println(b);
		
		
	
	
	}

}
